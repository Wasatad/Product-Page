<script>
import { mapMutations, mapState, mapGetters } from "vuex";
export default {
  data() {
    return {
      personActive: true,
      companyActive: false,
      deliveryOption: false,
      showLabel: true,
      shipping: 0,
      noPaymentInfo: false,

      //Customer data
      name: "",
      tel: "",
      country: "",
      city: "",
      zip: "",
      adress: "",
      businessName: "",
      vat: "",

      //CSS
      active: "active",
      tab: "tab",
      deliveryActive: "delivery-option-active",
      deliveryDisabled: "delivery-option",
      checkout: "checkout",
      checkoutDisabled: "checkout-disabled",
    };
  },
  methods: {
    ...mapMutations([
      "toggleCreditModal",
      "toggleCongratsModal",
      "addClient",
      "setTotal",
    ]),
    setPersonActive() {
      this.personActive = true;
      this.companyActive = false;
    },
    setCompanyActive() {
      this.personActive = false;
      this.companyActive = true;
    },
    label() {
      let activeOption = document.querySelector(".delivery-option-active");
      let deliveryPrice = activeOption.querySelector(".price");

      if (deliveryPrice.innerText == "$0.00") {
        this.showLabel = 0;
      } else {
        this.showLabel = 1;
      }
    },
    toggleDeliveryOption(e) {
      if (e.target.closest(".delivery-option")) {
        this.deliveryOption = !this.deliveryOption;
        this.label();

        this.shipping = document
          .querySelector(".delivery-option .price")
          .innerText.split(".")[0]
          .split("$")[1];
      } else {
        return;
      }
    },
    deleteFromCart(id) {
      this.$store.dispatch("deleteFromCart", id);
      this.$router.push("/");
    },
    showCongrats() {
      if (this.noCards) {
        let cardPlaceholder = document.querySelector(".card-placeholder");
        cardPlaceholder.style.border = "1px dashed red";
      } else {
        let client = {};
        client.name = this.name;
        client.tel = this.tel;
        client.country = this.country;
        client.city = this.city;
        client.zip = this.zip;
        client.adress = this.adress;
        client.businessName = this.businessName;
        client.vat = this.vat;
        this.addClient(client);
        this.setTotal(this.cartTotal);
        this.toggleCongratsModal();
      }
    },
  },
  computed: {
    ...mapState(["savedCards"]),
    ...mapGetters(["noCards"]),
    cart() {
      return this.$store.state.cart;
    },
    totalAmount() {
      return this.$store.getters.totalAmount;
    },
    totalPrice() {
      return `${
        this.$store.state.cart[0].price.split(".")[0].split("$")[1] *
        this.totalAmount
      }`;
    },
    cartTotal() {
      return +this.totalPrice + +this.shipping;
    },
    btnClass() {
      if (this.companyActive) {
        if (
          this.businessName == "" ||
          this.vat == "" ||
          this.country == "" ||
          this.city == "" ||
          this.zip == "" ||
          this.adress == "" ||
          this.savedCards.length == 0
        ) {
          return false;
        } else return true;
      } else {
        if (
          this.name == "" ||
          this.tel == "" ||
          this.country == "" ||
          this.city == "" ||
          this.zip == "" ||
          this.adress == "" ||
          this.savedCards.length == 0
        ) {
          return false;
        } else return true;
      }
    },
  },
  mounted() {
    const slider = document.querySelector(".card-container");
    let isDown = false;
    let startX;
    let scrollLeft;

    slider.addEventListener("mousedown", (e) => {
      isDown = true;
      startX = e.pageX - slider.offsetLeft;
      scrollLeft = slider.scrollLeft;
    });
    slider.addEventListener("mouseleave", () => {
      isDown = false;
    });
    slider.addEventListener("mouseup", () => {
      isDown = false;
    });
    slider.addEventListener("mousemove", (e) => {
      if (!isDown) return;
      e.preventDefault();
      const x = e.pageX - slider.offsetLeft;
      const walk = x - startX;
      slider.scrollLeft = scrollLeft - walk;
    });
  },
};
</script>

